def build_fee_analysis(spark, silver_path, fee_lookup_path, gold_output_dir, run_date):
    silver_df = spark.read.format("delta").load(silver_df)
    fee_rates_df = spark.read.csv(fee_lookup_path, header=True, inferSchema=True).withColumnRenamed("payment_method", "payment_method").withColumnRenamed("fee_rate", "fee_rate")
    fee_rates_df = broadcast(fee_rates_df)
    
    # Calculate gross revenue and transaction volume by payment method
    gross_revenue_df = silver_df.filter(col("status") == "COMPLETED") \
        .groupBy("payment_method") \
       .agg(_sum("amount").alias("gross_revenue"))
    
    txn_volume_df = silver_df.filter(col("status") == "COMPLETED") \
       .groupBy("payment_method") \
       .agg(_count("*").alias("txn_volume"))
    
    # Join with fee rates and calculate fee amount and effective revenue
    fee_analysis_df = gross_revenue_df.join(fee_rates_df, on="payment_method", how="left") \
       .withColumn("fee_amount", col("gross_revenue") * col("fee_rate")) \
       .withColumn("effective_revenue", col("gross_revenue") * (1 - coalesce(col("fee_rate"), 0)))
    
    # Add analysis date and write to Gold
    fee_analysis_df = fee_analysis_df.withColumn("analysis_date", lit(run_date))
    
    fee_analysis_df.write.partitionBy("analysis_date").mode("overwrite").parquet(f"{gold_output_dir}/fee_analysis")

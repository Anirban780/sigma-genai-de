def build_merchant_performance(spark, silver_path, output_path, run_date):
    silver_df = spark.read.format("delta").load(silver_path)
    
    merchant_df = silver_df.filter(col("status") == "COMPLETED") \
        .groupBy("merchant_id", "merchant_name", "category", "city", "transaction_date") \
       .agg(
            _sum("amount").alias("total_revenue"),
            _count("*").alias("txn_count"),
            _count(when(col("status") == "FAILED", 1)).alias("failed_count")
        ) \
       .withColumn("failure_rate_pct", col("failed_count") / col("txn_count") * 100) \
        .withColumnRenamed("transaction_date", "date")
    
    merchant_df.write.partitionBy("date").mode("overwrite").parquet(output_path)

def build_customer_ltv(spark, silver_path, output_path):
    silver_df = spark.read.format("delta").load(silver_path)
    
    customer_df = silver_df.filter(col("status") == "COMPLETED") \
       .groupBy("customer_id") \
        .agg(
            _sum("amount").alias("total_spent"),
            _count("*").alias("total_txns"),
            _max("transaction_date").alias("last_txn_date"),
            expr("first(transaction_date)").alias("first_txn_date")
        )
    
    # Calculate preferred payment method (mode)
    payment_method_counts = silver_df \
       .groupBy("customer_id", "payment_method") \
       .agg(_count("*").alias("count")) \
        .groupBy("customer_id") \
       .agg(_max("count").alias("max_count"), _max("payment_method").alias("preferred_payment_method"))
    
    customer_df = customer_df.join(payment_method_counts, on="customer_id", how="left") \
        .withColumn("avg_txn_value", col("total_spent") / col("total_txns"))
    
    customer_df.write.mode("overwrite").parquet(output_path)

def build_daily_summary(spark, silver_path, output_path, run_date):
    silver_df = spark.read.format("delta").load(silver_path)
    
    daily_summary_df = silver_df.filter(col("transaction_date") == run_date) \
        .groupBy("transaction_date") \
        .agg(
            _sum("amount").alias("total_revenue"),
            _count("*").alias("total_txns"),
            _count(when(col("status") == "COMPLETED", 1)).alias("completed_txns"),
            _count(when(col("status") == "FAILED", 1)).alias("failed_txns"),
            _count(col("customer_id").isNotNull()).alias("unique_customers"),
            _count(col("merchant_id").isNotNull()).alias("unique_merchants")
        ) \
       .withColumn("failure_rate_pct", col("failed_txns") / col("total_txns") * 100) \
       .withColumnRenamed("transaction_date", "date")
    
    daily_summary_df.write.partitionBy("date").mode("overwrite").parquet(output_path)

